void StartDefaultTask(void const *argument)
{
    double DutyA = 0.200;
    double DutyB = 0.800;
    PWMA.SetDuty(DutyA);
    PWMA.PWM_ON();
    PWMB.SetDuty(DutyB);
    PWMB.PWM_ON();

    while (true) {

        vTaskDelay(500);
    }

    vTaskDelete(nullptr);
}

